botaocoxinhas.addEventListener('click',function(){

    if (quantidadecoxinhas.style.display === 'block') {
        quantidadecoxinhas.style.display = 'none';
        coxinhas.style.display = 'none';
        coxinhas.style.backgroundColor = '#ffa500';
        botaocoxinhas.style.backgroundColor = '#ffa500';
        botaocoxinhas.style.width = '31vw';
        quantidadecoxinhas.reset();
    } else {
        quantidadecoxinhas.style.display = 'block';
        coxinhas.style.display = 'block';
        coxinhas.style.backgroundColor = '#088c19';
        botaocoxinhas.style.backgroundColor = '#088c19';
        botaocoxinhas.style.width = '31vw';
    }
})

